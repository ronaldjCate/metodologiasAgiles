package clases;

public class Seccion {
	public int cod_Seccion,cant_max_pers,cant_min_pers; 
	public String nom_seccion,estado,codigoSeccion,tipo;
	public String nombreSeccion;
	
	
	
	
	public String getNombreSeccion() {
		return nombreSeccion;
	}
	public void setNombreSeccion(String nombreSeccion) {
		this.nombreSeccion = nombreSeccion;
	}
	public int getCod_Seccion() {
		return cod_Seccion;
	}
	public void setCod_Seccion(int cod_Seccion) {
		this.cod_Seccion = cod_Seccion;
	}
	public int getCant_max_pers() {
		return cant_max_pers;
	}
	public void setCant_max_pers(int cant_max_pers) {
		this.cant_max_pers = cant_max_pers;
	}
	public int getCant_min_pers() {
		return cant_min_pers;
	}
	public void setCant_min_pers(int cant_min_pers) {
		this.cant_min_pers = cant_min_pers;
	}
	public String getNom_seccion() {
		return nom_seccion;
	}
	public void setNom_seccion(String nom_seccion) {
		this.nom_seccion = nom_seccion;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getCodigoSeccion() {
		return codigoSeccion;
	}
	public void setCodigoSeccion(String codigoSeccion) {
		this.codigoSeccion = codigoSeccion;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	
}
